#include <iostream>
using namespace std;

int main(){
    unsigned int a = 2, b = 6;
    cout<<"a = "<<a<<","<<"b = "<<b;
    cout<<"a&b = "<<(a&b)<<endl;
    
    cout<<"a|b = "<<(a|b)<<endl;

    cout<<"~a = "<<(~a)<<endl;

    cout<<"a<<3 ="<<(a<<3)<<endl;

    cout<<"b>>2 ="<<(b>>2)<<endl;

    cout<<"~b ="<<(~b)<<endl;

    return 0;


}